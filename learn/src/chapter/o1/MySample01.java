/*
 1、一个简单的Java程序
 Java区分大小写。如果出现了大小写拼写错误(例如，将main 拼写成Main), 程序将无法运行。
 关键字public称为访问修饰符,这些修饰符用于控制程序的其他部分对这段代码的访问级别
 关键字class表明Java程序中的全部内容都包含在类中。
 类是所有Java应用的构建模块。Java程序中的所有内容都必须放在类中。
 关键字class后面紧跟类名。Java中定义类名的规则很宽松。
 类名必须以字母开头，后面可以跟字母和数字的任意组合。
 标准命名约定为：类名是以大写字母开头的名词。
 如果名字由多个单词组成，每个单词的第一个字母都应该大写。
 源代码的文件名必须与公共类的类名相同，并用.java作为扩展名
 每个Java应用都必须有一个 main方法
 在Java中，每个语句必须用分号结束
*/

